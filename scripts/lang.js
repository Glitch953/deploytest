let currentLang = 'ar';
let currentTheme = localStorage.getItem('theme') || 'dark';

async function applyLanguage(lang = 'ar') {
    // Force Arabic
    currentLang = 'ar';
    localStorage.setItem('lang', 'ar');

    document.documentElement.lang = 'ar';
    document.documentElement.dir = 'rtl';

    // Apply translations to elements with data-i18n (keeping icons if present)
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations.ar[key] !== undefined) {
            const icon = el.querySelector('i');
            if (icon) {
                const iconHtml = icon.outerHTML;
                el.innerHTML = `${iconHtml} ${translations.ar[key]}`;
            } else {
                el.textContent = translations.ar[key];
            }
        }
    });

    // Update placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (translations.ar[key]) {
            el.placeholder = translations.ar[key];
        }
    });
}

function toggleLanguage() {
    // Language toggle disabled, enforce Arabic
    applyLanguage('ar');
}

async function applyTheme(theme, syncWithServer = true) {
    currentTheme = theme;
    localStorage.setItem('theme', theme);

    const token = localStorage.getItem('token');
    if (token && syncWithServer) {
        try {
            const apiBaseUrl = (window.location.hostname === 'localhost' || window.location.hostname === '127.0.0.1' || window.location.protocol === 'file:')
                ? 'http://localhost:5000/api'
                : 'https://eyf-website-production.up.railway.app/api';
            await fetch(`${apiBaseUrl}/user/preferences`, {
                method: 'PUT',
                headers: {
                    'Content-Type': 'application/json',
                    'Authorization': `Bearer ${token}`
                },
                body: JSON.stringify({ theme: theme })
            });
        } catch (e) {
            console.error('Error syncing theme:', e);
        }
    }
    document.documentElement.setAttribute('data-theme', theme);

    const themeIcon = document.getElementById('theme-icon');
    if (themeIcon) {
        themeIcon.className = theme === 'dark' ? 'fas fa-moon' : 'fas fa-sun';
    }
}

function toggleTheme() {
    const newTheme = currentTheme === 'dark' ? 'light' : 'dark';
    applyTheme(newTheme);
}

async function loadPreferencesFromServer() {
    const token = localStorage.getItem('token');
    if (!token) return;

    try {
        const apiBaseUrl = (window.location.hostname === 'localhost' || window.location.hostname === '127.0.0.1' || window.location.protocol === 'file:')
            ? 'http://localhost:5000/api'
            : 'https://eyf-website-production.up.railway.app/api';
        const response = await fetch(`${apiBaseUrl}/user/me`, {
            headers: { 'Authorization': `Bearer ${token}` }
        });
        if (response.ok) {
            const user = await response.json();
            if (user.preferences) {
                // Ignore language preference, force Arabic
                if (user.preferences.theme && user.preferences.theme !== currentTheme) {
                    applyTheme(user.preferences.theme, false);
                }
            }
        }
    } catch (e) {
        console.error('Error loading preferences:', e);
    }
}

document.addEventListener('DOMContentLoaded', function () {
    applyLanguage('ar');
    applyTheme(currentTheme, false);
    loadPreferencesFromServer();
});
